const add = (a,b)=>{
    return a+b
}
const mul = (a,b)=>{
    return a*b
}
const div = (a,b)=>{
    return a/b
}
const sub = (a,b)=>{
    return a-b
}
const pow = (a,b)=>{
    return a**b
}
const sqr = (a)=>{
    return a**2
}
const cube = (a)=>{
    return a**3
}
module.exports = {add,sub,mul,div,pow,sqr,cube}